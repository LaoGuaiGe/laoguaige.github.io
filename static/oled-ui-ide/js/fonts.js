let HZK12 = null, HZK16 = null, HZK20 = null, U2G_MAP = null;

async function loadChineseFonts(basePath) {
    try {
        const [h12, h16, h20, map] = await Promise.all([
            fetch(basePath + "/HZK12.bin").then(r => r.arrayBuffer()),
            fetch(basePath + "/HZK16.bin").then(r => r.arrayBuffer()),
            fetch(basePath + "/HZK20.bin").then(r => r.arrayBuffer()),
            fetch(basePath + "/unicode_gb2312_map.bin").then(r => r.arrayBuffer()),
        ]);
        HZK12 = new Uint8Array(h12);
        HZK16 = new Uint8Array(h16);
        HZK20 = new Uint8Array(h20);
        U2G_MAP = new Uint8Array(map);
        console.log("Chinese fonts loaded");
    } catch(e) { console.warn("Chinese fonts not loaded:", e); }
}

function unicodeToGB2312(code) {
    if (code < 0x4E00 || code > 0x9FFF || !U2G_MAP) return 0;
    const idx = (code - 0x4E00) * 2;
    return (U2G_MAP[idx] << 8) | U2G_MAP[idx + 1];
}

function getChineseCharData(code, fontSize) {
    const gb = unicodeToGB2312(code);
    if (!gb) return null;
    const hi = (gb >> 8) & 0xFF;
    const lo = gb & 0xFF;
    const offset = ((hi - 0xA1) * 94 + (lo - 0xA1));
    let font, charSize;
    switch(fontSize) {
        case 12: font = HZK12; charSize = 24; break;
        case 16: font = HZK16; charSize = 32; break;
        case 20: font = HZK20; charSize = 60; break;
        default: font = HZK16; charSize = 32; break;
    }
    if (!font || offset < 0 || offset * charSize + charSize > font.length) return null;
    return font.slice(offset * charSize, offset * charSize + charSize);
}

function getFont(size) {
    switch(size) {
        case 6: return { width: 6, height: 8, dataWidth: 6, dataHeight: 8, chars: F6x8 };
        case 7: return { width: 7, height: 12, dataWidth: 6, dataHeight: 8, chars: F6x8 };
        case 8: return { width: 8, height: 16, dataWidth: 8, dataHeight: 16, chars: F8x16 };
        case 10: return { width: 10, height: 20, dataWidth: 8, dataHeight: 16, chars: F8x16 };
        case 12: return { width: 7, height: 12, dataWidth: 6, dataHeight: 8, chars: F6x8 };
        case 16: return { width: 8, height: 16, dataWidth: 8, dataHeight: 16, chars: F8x16 };
        case 20: return { width: 10, height: 20, dataWidth: 8, dataHeight: 16, chars: F8x16 };
        default: return { width: 6, height: 8, dataWidth: 6, dataHeight: 8, chars: F6x8 };
    }
}

function hasChinese() { return HZK16 !== null; }

